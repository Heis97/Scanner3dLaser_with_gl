        public VoxelToStlGpu(GraphicGL graphic,int width = 200, int height = 200, int depth = 200)
        {
            // Инициализация OpenGL (контекст уже должен быть создан)
            Width = width;
            Height = height;
            Depth = depth;
            ComputeMaxCounts();
            CreateBuffers();
            
            var CompShaderGL = graphic.assembCode(new string[] { @"Graphic\Shaders\Comp\from_voxels_mesh.glsl" });
            computeProgram = graphic.createShaderCompute(CompShaderGL);
            LoadTriTable();
        }

        private void ComputeMaxCounts()
        {
            // Грубая оценка: максимальное количество треугольников ≈ количество кубов × 2
            uint cubeCount = ((uint)Width - 1) * ((uint)Height - 1) * ((uint)Depth - 1); // ~249 млн
            uint maxTriangles = cubeCount * 2;   // ~498 млн
            maxVertexCount = maxTriangles * 3;
            maxIndexCount = maxTriangles * 3;
        }

        private void CreateBuffers()
        {
            // Буфер входных вокселей (uint, 0 или 1)
            voxelDataSize = (uint)Width * (uint)Height * (uint)Depth * sizeof(uint);
            voxelSSBO = Gl.GenBuffer();
            Gl.BindBuffer(BufferTarget.ShaderStorageBuffer, voxelSSBO);
            Gl.BufferData(BufferTarget.ShaderStorageBuffer, voxelDataSize, IntPtr.Zero, BufferUsage.DynamicDraw);

            // Буфер выходных вершин (vec3 = 3 float)
            vertexSSBO = Gl.GenBuffer();
            Gl.BindBuffer(BufferTarget.ShaderStorageBuffer, vertexSSBO);
            Gl.BufferData(BufferTarget.ShaderStorageBuffer, maxVertexCount * 3 * sizeof(float), IntPtr.Zero, BufferUsage.DynamicDraw);

            // Буфер выходных индексов (uint)
            indexSSBO = Gl.GenBuffer();
            Gl.BindBuffer(BufferTarget.ShaderStorageBuffer, indexSSBO);
            Gl.BufferData(BufferTarget.ShaderStorageBuffer, maxIndexCount * sizeof(uint), IntPtr.Zero, BufferUsage.DynamicDraw);


            // Счётчик вершин (один uint)
            vertexCounterSSBO = Gl.GenBuffer();
            Gl.BindBuffer(BufferTarget.ShaderStorageBuffer, vertexCounterSSBO);
            Gl.BufferData(BufferTarget.ShaderStorageBuffer, sizeof(uint), IntPtr.Zero, BufferUsage.DynamicDraw);

            // Счётчик индексов (один uint)
            indexCounterSSBO = Gl.GenBuffer();
            Gl.BindBuffer(BufferTarget.ShaderStorageBuffer, indexCounterSSBO);
            Gl.BufferData(BufferTarget.ShaderStorageBuffer, sizeof(uint), IntPtr.Zero, BufferUsage.DynamicDraw);


            voxelDataSize = (uint)Width * (uint)Height * (uint)Depth * sizeof(uint);
            debugTableSSBO = Gl.GenBuffer();
            Gl.BindBuffer(BufferTarget.ShaderStorageBuffer, debugTableSSBO);
            Gl.BufferData(BufferTarget.ShaderStorageBuffer, voxelDataSize, IntPtr.Zero, BufferUsage.DynamicDraw);

            // Сбросить привязку
            Gl.BindBuffer(BufferTarget.ShaderStorageBuffer, 0);
        }

