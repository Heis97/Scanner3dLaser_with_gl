            // Каждый треугольник: нормаль (3 float) + 3 вершины (3 float каждая) + 2 байта атрибут
            int triangleCount = indices.Count / 3;
            using (BinaryWriter writer = new BinaryWriter(File.Open(filename, FileMode.Create)))
            {
